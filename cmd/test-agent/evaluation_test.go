package main

import (
	"context"
	"encoding/json"
	"os"
	"path/filepath"
	"testing"
	"time"

	"uap"
)

func TestRunEvaluationStaticAndDynamicCollections(t *testing.T) {
	gw := newTestGateway(t)
	defer gw.close()

	echoAgent := uap.NewClient(gw.wsURL(), "echo-agent", "echo", "echo-agent")
	echoAgent.Tools = []uap.ToolDef{{Name: "Echo"}}
	echoAgent.OnMessage = func(msg *uap.Message) {
		if msg.Type != uap.MsgToolCall {
			return
		}
		var payload uap.ToolCallPayload
		if err := json.Unmarshal(msg.Payload, &payload); err != nil {
			t.Errorf("echoAgent unmarshal tool call: %v", err)
			return
		}
		var args map[string]any
		_ = json.Unmarshal(payload.Arguments, &args)
		text, _ := args["text"].(string)
		_ = echoAgent.SendTo(msg.From, uap.MsgToolResult, uap.ToolResultPayload{
			RequestID: msg.ID,
			Success:   true,
			Result:    `{"echo":"` + text + `"}`,
		})
	}
	go echoAgent.Run()
	defer echoAgent.Stop()

	plannerAgent := uap.NewClient(gw.wsURL(), "llm-agent", "planner", "llm-agent")
	plannerAgent.OnMessage = func(msg *uap.Message) {
		if msg.Type != uap.MsgTaskAssign {
			return
		}
		var payload uap.TaskAssignPayload
		if err := json.Unmarshal(msg.Payload, &payload); err != nil {
			t.Errorf("plannerAgent unmarshal task_assign: %v", err)
			return
		}
		result := mustMarshal(map[string]any{
			"id":          "dynamic-suite",
			"title":       "dynamic-suite",
			"description": "generated by fake llm-agent",
			"scenarios": []map[string]any{
				{
					"id":          "dynamic-echo",
					"title":       "dynamic-echo",
					"description": "dynamic echo",
					"category":    "dynamic",
					"priority":    "P1",
					"tags":        []string{"dynamic", "echo"},
					"entry": map[string]any{
						"type":     EntryTypeToolCall,
						"to_agent": "echo-agent",
						"tool": map[string]any{
							"tool_name": "Echo",
							"arguments": map[string]any{"text": "dynamic hello"},
						},
					},
					"assertions": map[string]any{
						"expect_message_type": uap.MsgToolResult,
						"result_contains":     []string{"dynamic hello"},
						"require_agents":      []string{"echo-agent"},
						"require_msg_types":   []string{uap.MsgToolCall, uap.MsgToolResult},
						"min_trace_events":    2,
					},
				},
				{
					"id":          "dynamic-offline",
					"title":       "dynamic-offline",
					"description": "should be skipped",
					"category":    "dynamic",
					"priority":    "P2",
					"tags":        []string{"dynamic", "skip"},
					"entry": map[string]any{
						"type":     EntryTypeToolCall,
						"to_agent": "missing-agent",
						"tool": map[string]any{
							"tool_name": "Echo",
							"arguments": map[string]any{"text": "skip me"},
						},
					},
					"assertions": map[string]any{
						"expect_message_type": uap.MsgToolResult,
						"require_agents":      []string{"missing-agent"},
						"require_msg_types":   []string{uap.MsgToolCall, uap.MsgToolResult},
						"min_trace_events":    2,
					},
				},
			},
		})
		_ = plannerAgent.SendTo(msg.From, uap.MsgTaskComplete, uap.TaskCompletePayload{
			TaskID: payload.TaskID,
			Status: "success",
			Result: string(result),
		})
	}
	go plannerAgent.Run()
	defer plannerAgent.Stop()

	time.Sleep(300 * time.Millisecond)

	suiteDir := t.TempDir()
	staticSuitePath := filepath.Join(suiteDir, "static.json")
	staticSuite := mustMarshal(map[string]any{
		"id":          "static-suite",
		"title":       "static-suite",
		"description": "fixed suite",
		"scenarios": []map[string]any{
			{
				"id":          "static-echo",
				"title":       "static-echo",
				"description": "static echo",
				"category":    "contract",
				"priority":    "P0",
				"entry": map[string]any{
					"type":     EntryTypeToolCall,
					"to_agent": "echo-agent",
					"tool": map[string]any{
						"tool_name": "Echo",
						"arguments": map[string]any{"text": "static hello"},
					},
				},
				"assertions": map[string]any{
					"expect_message_type": uap.MsgToolResult,
					"result_contains":     []string{"static hello"},
					"require_agents":      []string{"echo-agent"},
					"require_msg_types":   []string{uap.MsgToolCall, uap.MsgToolResult},
					"min_trace_events":    2,
				},
			},
		},
	})
	if err := os.WriteFile(staticSuitePath, staticSuite, 0644); err != nil {
		t.Fatalf("write static suite: %v", err)
	}

	cfg := DefaultConfig()
	cfg.GatewayURL = gw.wsURL()
	cfg.GatewayHTTP = gw.httpURL()
	cfg.OutputDir = t.TempDir()
	cfg.SuiteDir = suiteDir
	cfg.StaticSuiteDir = suiteDir
	cfg.DefaultTimeoutSec = 5
	cfg.Dynamic.Enabled = true
	cfg.Dynamic.GeneratorAgent = "llm-agent"
	cfg.Dynamic.TimeoutSec = 5
	cfg.Dynamic.MaxScenarios = 2

	runner := NewRunner(cfg)
	defer runner.Close()

	final, err := runner.RunEvaluation(context.Background())
	if err != nil {
		t.Fatalf("RunEvaluation error: %v", err)
	}
	if final.StaticReport == nil || final.DynamicReport == nil {
		t.Fatalf("expected static and dynamic reports, got %+v", final)
	}
	if final.StaticReport.ExecutedScenarios != 1 || final.StaticReport.PassedScenarios != 1 {
		t.Fatalf("unexpected static report: %+v", final.StaticReport)
	}
	if final.DynamicReport.ExecutedScenarios != 1 || final.DynamicReport.PassedScenarios != 1 {
		t.Fatalf("unexpected dynamic report: %+v", final.DynamicReport)
	}
	if final.DynamicReport.SkippedScenarios != 1 {
		t.Fatalf("expected 1 skipped dynamic scenario, got %+v", final.DynamicReport)
	}
	if final.OverallScore <= 0 {
		t.Fatalf("expected overall score > 0, got %+v", final)
	}

	finalReportPath := filepath.Join(cfg.OutputDir, time.Now().Format("2006-01-02"), final.RunID, "final_report.json")
	if _, err := os.Stat(finalReportPath); err != nil {
		t.Fatalf("expected final report on disk: %v", err)
	}
}
